#import "UIColor+TwoByTwo.h"


@implementation UIColor (TwoByTwo)

+ (UIColor *)appBlackishColor
{
    return [UIColor colorWithRed:41.0/255 green:41.0/255 blue:41.0/255 alpha:1.0];
}

+ (UIColor *)appDarkGrayColor
{
    return [UIColor colorWithRed:102.0/255 green:102.0/255 blue:102.0/255 alpha:1.0];
}

+ (UIColor *)appRedColor
{
    return [UIColor colorWithRed:255.0/255 green:102.0/255 blue:102.0/255 alpha:1.0];
}

@end
